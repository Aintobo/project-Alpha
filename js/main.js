
function search(){
    var view = document.getElementById('search_view');
    if(view.style.display == 'block'){
        view.style.display = 'none'
    }
    else{
        view.style.display = 'block'
    }

    
}
function search_close(){
    var view = document.getElementById('search_view');
    view.style.display = 'none'
}

